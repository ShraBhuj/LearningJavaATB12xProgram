package JavaCodeChallenge;

public class variableDeclare {
    public static void main(String[] args) {
        // Declare and initialize primitive data type variables
        int myAge = 20;
        double mySalary = 99.99;
        boolean myGender = true;
        char myName = 'A';

        // Print the values
        System.out.println("My Age: " + myAge);
        System.out.println("My Salary: $" + mySalary);
        System.out.println("I'm I a human? " + myGender);
        System.out.println("My Name: " + myName);
    }
    }

